// Поточна мова
let currentLanguage = 'uk';

// Функція для зміни мови
function switchLanguage(lang) {
    currentLanguage = lang;
    document.documentElement.lang = lang;
    
    // Оновлюємо кнопки мови
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        }
    });
    
    // Оновлюємо переклади
    updateTranslations();
    
    // Зберігаємо вибір мови
    localStorage.setItem('farmerama-language', lang);
}

// Функція для оновлення перекладів
function updateTranslations() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            element.textContent = translations[currentLanguage][key];
        }
    });
}

// Ініціалізація мови при завантаженні сторінки
function initializeLanguage() {
    // Перевіряємо збережену мову
    const savedLanguage = localStorage.getItem('farmerama-language');
    if (savedLanguage && translations[savedLanguage]) {
        currentLanguage = savedLanguage;
    }
    
    // Встановлюємо мову
    switchLanguage(currentLanguage);
}

// Експорт для використання в інших файлах
window.translations = translations;
window.switchLanguage = switchLanguage;
window.initializeLanguage = initializeLanguage; 